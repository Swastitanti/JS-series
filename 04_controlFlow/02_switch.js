const month = 3 //"jan"
// similarly for strings
switch (month) {
    case 1: // we can write "jan" instead of 1
        console.log("January");
        break;
    case 2:
        console.log("February");
        break;
    case 3:
        console.log("March");
        break;
    case 4:
        console.log("April");
        break;
    case 5:
        console.log("May");
        break;

    default:
        console.log("Default case");
        break;
}



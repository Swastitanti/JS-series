// while Loop

let index = 0
while (index <= 10) {
    console.log(`value of index is ${index}`);
    index = index + 2 // terminate value
}

let myArray = ['flash', "batnam", "superman"]
let arr = 0
while (arr < myArray.length){
    console.log(`value is ${myArray[arr]}`);
    arr = arr + 1
}

let score = 1
do {
    console.log(`score is ${score}`);
    score++
} while (score <= 10);


